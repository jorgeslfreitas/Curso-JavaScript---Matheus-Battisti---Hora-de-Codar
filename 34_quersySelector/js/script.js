// Acessando o DOM com querySelector
let paragrafo = document.querySelector('#paragrafo-1'); // Acessa o DOM como usado no CSS, neste caso o querySelector está buscando pelo primeiro seletor que ele achar no documento, caso queira outro o ideal é especificar mais como usado no CSS novamente
console.log(paragrafo);

// Acessando o DOM com querySelectorAll
let lista = document.querySelectorAll('.item'); // Acessa o DOM como usado no CSS, neste caso o querySelectorAll está buscando por todo o seletor que ele encontrar
console.log(lista);


